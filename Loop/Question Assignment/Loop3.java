// Q3. Write a java program to print all alphabets from a to z. - using while loop

public class Loop3{
	
		public static void main(String args[]){
			for(char a ='A';a<='Z';a++){
				
				int no = (int)a;
				System.out.print(a+":"+no+ " ");
			}
			for(char a = 'a';a<='z';a++){
				
				System.out.print(a+" ");
			}
			for(int  a = 'a';a<='z';a++){
				
				System.out.print(a+" ");
			}
		}
	
}	