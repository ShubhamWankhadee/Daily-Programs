// Q1. Write a java program to print all natural numbers from 1 to n. using while loop

public class Q1{

	static void Natural(int no){
		System.out.println("Natural no is: ");
		 for(int i=0;i<=no;i++){
			System.out.println(i);
		 }
    }
		 
    public static void main(String[] args) {
         
		 Natural(10);
 
    }
}
